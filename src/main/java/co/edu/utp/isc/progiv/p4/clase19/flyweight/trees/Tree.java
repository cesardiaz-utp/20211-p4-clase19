package co.edu.utp.isc.progiv.p4.clase19.flyweight.trees;

import java.awt.Graphics;

/**
 *
 * @author cdiaz
 */
public class Tree {

    private final int x;
    private final int y;
    private final TreeType type;

    public Tree(int x, int y, TreeType type) {
        this.x = x;
        this.y = y;
        this.type = type;
    }

    public void draw(Graphics g) {
        type.draw(g, x, y);
    }
}
